    public void populateCarManufacturers(){
       int i = 1;
       for(Car c: carCatalog.getCarCatalog())
       {
           if(!manufacturerArr.contains(c.getManufacturerName()))
           {
               manufacturerArr.add(c.getManufacturerName());
               JCheckBox cb = new JCheckBox();
               cb.setSelected(true);
               cb.setLocation(5,i*20);
               cb.setSize(200,30);
               cb.setText(c.getManufacturerName());
               jPanelCarManufacturer.add(cb);
               cb.setName(c.getManufacturerName()+i);
               i++; 
           }  
       }
    }
    
    public void populateCarBrand(){
       int i = 1;
       for(Car c: carCatalog.getCarCatalog())
       {
           if((!brandArr.contains(c.getBrand())) && (manufacturerArr.contains(c.getManufacturerName())))
           {
               brandArr.add(c.getBrand());
               JCheckBox cb = new JCheckBox();
               cb.setSelected(true);
               cb.setLocation(5,i*20);
               cb.setSize(100,30);
               cb.setText(c.getBrand());
               jPanelBrand.add(cb);
               cb.setName(c.getBrand()+i);
               i++; 
               invalidate();
           }  
       }
    }
    
    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        dtm.setRowCount(0);
        for(Car c : carCatalog.getCarCatalog())
        {
           if(brandArr.contains(c.getBrand()) && manufacturerArr.contains(c.getManufacturerName())){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = c;
            row[1] = c.getManufacturerName();
            row[2] = c.getYearOfManufacture();
            row[3] = c.getModelNumber();
            row[4] = c.getSerialNumber();
            row[5] = c.getColor();
            row[6] = c.getCity();
            row[7] = c.getNumberOfSeats();
            if(c.getMaintenanceCertificate().equals("expired")){
                row[8] = "Expired";
            }else if(c.getMaintenanceCertificate().equals("notexpired")){
                row[8] = "Not Expired";
            }
            row[9] = c.isAvailability();
            dtm.addRow(row);
           }
        }
    }
