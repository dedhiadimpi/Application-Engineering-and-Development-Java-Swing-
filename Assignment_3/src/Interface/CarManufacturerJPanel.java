    private void jButtonSearchCarBrandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchCarBrandActionPerformed
        // TODO add your handling code here:
        jPanelBrand.removeAll();
        manufacturerArr.clear();
        jPanelBrand.updateUI();
        brandArr.clear();
        Component[] carManufacturerComponents = jPanelCarManufacturer.getComponents();
        for (Component c : carManufacturerComponents) {
            if(c instanceof JCheckBox){
              JCheckBox cb = (JCheckBox)c;
              if (cb.isSelected()){
                  manufacturerArr.add(cb.getText());
              }  
            }
        }
        populateCarBrand();
        
    }//GEN-LAST:event_jButtonSearchCarBrandActionPerformed

