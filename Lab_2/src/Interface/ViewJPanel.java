    private void jViewDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jViewDetailsBtnActionPerformed
        // TODO add your handling code here:
        int selectedRows = jVitalSignTable.getSelectedRow();
        
        if(selectedRows >=0){
            VitalSign vs = (VitalSign) jVitalSignTable.getValueAt(selectedRows, 0);
            jTemperatureTextField.setText(String.valueOf(vs.getTemperature()));
            jBloodPressureTextField.setText(String.valueOf(vs.getBloodPressure()));
            jPulseTextField.setText(String.valueOf(vs.getPulse()));
            jDateTextField.setText(vs.getDate());
        } else{
            JOptionPane.showMessageDialog(null, "Please select a Vital Sign");
        }
    }//GEN-LAST:event_jViewDetailsBtnActionPerformed

    private void jDeleteVitalSignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteVitalSignBtnActionPerformed
        // TODO add your handling code here:
        int selectedRows = jVitalSignTable.getSelectedRow();
        
        if(selectedRows >=0){
            VitalSign vs = (VitalSign) jVitalSignTable.getValueAt(selectedRows, 0);
            vsh.deleteVitalSign(vs);
            populateTable();
            JOptionPane.showMessageDialog(null, "Vital Sign Deleted successfully");
        } else{
            JOptionPane.showMessageDialog(null, "Please select a Vital Sign");
        }
    }//GEN-LAST:event_jDeleteVitalSignBtnActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        jTemperatureTextField.setEnabled(true);
        jBloodPressureTextField.setEnabled(true);
        jPulseTextField.setEnabled(true);
        jDateTextField.setEnabled(true);
        
        btnUpdate.setEnabled(false);
        btnConfirm.setEnabled(true);
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        int selectedRow = jVitalSignTable.getSelectedRow();
        
        if(selectedRow >= 0){
            VitalSign vs = (VitalSign) jVitalSignTable.getValueAt(selectedRow, 0);
            vs.setTemperature(Double.parseDouble(jTemperatureTextField.getText()));
            vs.setBloodPressure(Double.parseDouble(jBloodPressureTextField.getText()));
            vs.setPulse(Integer.parseInt(jPulseTextField.getText()));
            vs.setDate(jDateTextField.getText());
            
            populateTable();
            
            btnUpdate.setEnabled(true);
            btnConfirm.setEnabled(false);
            
            jTemperatureTextField.setEnabled(false);
            jBloodPressureTextField.setEnabled(false);
            jPulseTextField.setEnabled(false);
            jDateTextField.setEnabled(false);
            
            JOptionPane.showMessageDialog(null, "Vital Sign updated successfully");
        }
    }//GEN-LAST:event_btnConfirmActionPerformed


